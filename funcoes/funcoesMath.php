<?php

// Função de raiz quadrada - sqrt()
$arg = 81;
echo "A raíz de $arg é = " . sqrt($arg);
echo '<br>';
// Função Máximo e mínimo - max(), mim()
$values = [1, 2, 6, 9, 8, 7];
echo "O valor máximo é " . max($values);
echo '<br>';
echo "O valor mínimo é " . min($values);
echo '<br>';
// Função PI retorna o valor de PI
echo "O valor PI é " . pi();
echo '<br>';
// A função round() arredonda um número para o inteiro mais próximo:
$val = 3.141592;
echo "O valor arrendondado é " . round($val);
echo '<br>';
// A função rand() gera números aleatório;
$min = 100;
$max = 200;
echo "O sorteado entre 100 e 200 é " . rand($min, $max);






